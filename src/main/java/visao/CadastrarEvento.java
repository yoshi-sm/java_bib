    public boolean ConfirmaCamposObrigatorios(){
        if(txtnome.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane,"Campo Nome Obrigat贸rio","Aviso",JOptionPane.WARNING_MESSAGE);
            return false;
        }
        else if(txtdata.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane,"Campo Data Obrigat贸rio","Aviso",JOptionPane.WARNING_MESSAGE);
            return false;
        }
        else if(txtlocal.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane,"Campo Local Obrigat贸rio","Aviso",JOptionPane.WARNING_MESSAGE);
            return false;
        }
        else if(txtassunto.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane,"Campo Assunto Obrigat贸rio","Aviso",JOptionPane.WARNING_MESSAGE);
            return false;
        }
        else{
            return true;
        }
    }
    
